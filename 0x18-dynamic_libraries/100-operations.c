#include <stdio.h>

/**
 * add - adds integers together
 * @a: integer 1
 * @b: integer 2
 * Return: a+b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts integers
 * @a: integer 1
 * @b: integer 2
 * Return: a-b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies integers
 * @a: integer 1
 * @b: integer 2
 * Return: a*b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides integers
 * @a: integer 1
 * @b: integer 2
 * Return: a/b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - remainder of equation
 * @a: integer 1
 * @b: integer 2
 * Return:a%b
 */
int mod(int a, int b)
{
	return (a % b);
}
